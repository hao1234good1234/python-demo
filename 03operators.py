'''
一、算术运算符（用于数学计算）
'''
# | 运算符 | 名称       | 示例     | 结果  | 说明                     |
# | ------ | ---------- | -------- | ----- | ------------------------ |
# | `+`    | 加         | `5 + 3`  | `8`   |                          |
# | `-`    | 减         | `5 - 3`  | `2`   |                          |
# | `*`    | 乘         | `5 * 3`  | `15`  |                          |
# | `/`    | 除（真除） | `5 / 2`  | `2.5` | **结果总是 float**       |
# | `//`   | 除（整除） | `5 // 2` | `2`   | 向下取整（地板除）       |
# | `%`    | 取余       | `5 % 2`  | `1`   | 求余数（常用于判断奇偶） |
# | `**`   | 幂         | `2 ** 3` | `8`   | 2 的 3 次方              |

a = 10
b = 3

print("加法：", a + b)
print("减法：", a - b)
print("乘法：", a * b)
print("除法（真除）：", a / b)
print("除法（整除）：", a // b)
print("取余：", a % b)
print("幂运算：", a ** b)

print("加法：", 5 + 2) # 7
print("加法（负数）：", -5 + 2) # -3
print("减法：", 5 - 2) # 3
print("减法（负数）：", -5 - 2) # -7
print("乘法：", 5 * 2) # 10
print("乘法（负数）：", -5 * 2) # -10
print("真除：", 5 / 2) # 2.5
print("真除（负数）：", -5 / 2)  # -2.5
print("整除：", 5 // 2) # 2
print("整除（负数）：", -5 // 2) # -3
print("取余：", 5 % 2) # 1
print("取余（负数）：", -5 % 2) # 1
print("幂运算：", 2 ** 3) # 8
print("幂运算（负指数）：", 2 ** -3) # 0.125

#  重点注意：
# / 永远返回浮点数，即使能整除：4 / 2 → 2.0
# // 是“向下取整”，对负数要小心：
# print(-7 // 3)  # 输出 -3（不是 -2！）

'''
二、比较运算符（用于判断，返回布尔值）
'''
x = 10 
y = 20

print(x == y) # False
print(x != y) # True
print(x > y)  # False
print(x < y)  # True
print(x >= y) # False
print(x <= y) # True

'''
三、逻辑运算符（组合多个判断）
'''
age = 17
has_id = True
# 判断是否可以进入酒吧（年龄≥18 且 有身份证）
if age >= 18 and has_id:
    print("欢迎光临！")
# 判断是否需要帮助（年龄<18 或 没有身份证）
if age < 18 or not has_id:
    print("需要帮助吗？")


'''
四、运算符优先级（谁先算？）
'''

# Python 运算顺序和数学一致，但建议用括号明确优先级！

# 从高到低（记住关键几条即可）：

# () 括号（最高优先级）
# ** 幂
# * / // % 乘除取余
# + - 加减
# 比较运算符（== != > < 等）
# not
# and
# or（最低）

result = 3 + 4 * 2 # 先乘后加，结果是 11
result = (3 + 4) * 2 # 括号优先，结果是 14

print(True or False and False) # 先算 and，结果是 True
print((True or False) and False) # 括号优先，结果是 False


