"""
集合（set） → 去重、交集、并集、差集
"""
# 特点：
# - **元素唯一**（自动去重）  
# - **无序**（不能索引）  
# - **支持交集、并集、差集等运算**

# 一、什么是集合（set）？
# - **无序**：元素没有固定顺序（不能用 `s[0]` 访问）
# - **唯一**：自动去除重复元素
# - **可变**：可以添加/删除元素（但元素本身必须**不可变**）
# - **元素必须是可哈希的**（如 `int`, `str`, `tuple`，不能是 `list`/`dict`）
# ✅ **典型用途**：
# - 去重（如从列表中去除重复项）
# - 快速成员判断（比列表快得多！）
# - 数学集合运算：交集、并集、差集、对称差集

#  二、创建集合
# 1. 基本语法：**花括号 `{}`（但不能空！）**
# ❌ 空花括号是字典！
empty_dict = {}  # 这是 dict！
# 创建空集合
empty_set = set()
# 创建非空集合
fruits = {"apple", "banana", "cherry"}
numbers = {1, 2, 3, 2, 1}  # 自动去重 
print(numbers)  # {1, 2, 3}

# 2. 用 `set()` 构造函数（推荐用于去重）
# 从列表去重
lst = [1, 2, 2, 3, 3, 3]
unique = set(lst)
print(unique)  # {1, 2, 3}

# 从字符串去重
letters = set("hello")
print(letters)  # {'h', 'e', 'l', 'o'}

# 3. 用 `set()` 构造函数（推荐用于去重）
# 从元组去重
tup = (1, 2, 2, 3, 3, 3)
unique = set(tup)
print(unique)  # {1, 2, 3}

# ⚠️ **注意**：  

# - `{}` 是空字典，**不是空集合**！  
# - 空集合必须写成 `set()`
